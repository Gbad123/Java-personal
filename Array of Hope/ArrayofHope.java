public class ArrayofHope
 {
     
  public static void main(String args[])
  {
      
   //prints the deicmal
   System.out.println("Decimal");
   
   for(int a = 65; a <= 90; a++)
   {
     
     //all the numbers
     System.out.print(a + ",");
     
   }
   
   {
       
     //prints the space in between
     System.out.print("\n");
     System.out.print("\n");
     
   }
   
   {
       
       //same things as above but alphabets instead of numbers
       System.out.println("Character");
       
    for(int j = 65; j <= 90; j++)
    {
        
        System.out.print((char)j + ",");
        
    }
    
  }
  
 }
 
}